-- 2. ORDERS --------------------------------------------
create table if not exists orders (
  id bigint generated by default as identity primary key,
  order_number text unique not null,
  customer_name text not null,
  phone text not null,
  email text,
  address text not null,
  notes text,
  items jsonb not null,          -- [{ id, name, price, qty }, ...]
  subtotal numeric(10,2) not null,
  status text not null default 'new',   -- new / processing / shipped / delivered / cancelled
  created_at timestamptz not null default now()
);

alter table orders enable row level security;

-- Anyone can place an order (public storefront, no login required)
create policy "Public can insert orders"
on orders for insert
to anon
with check (true);

-- Note: SELECT is intentionally NOT open to anon, so customers can't browse
-- each other's orders. View/manage orders from the Supabase Table Editor,
-- or add an authenticated policy later if you build an admin dashboard.


-- 3. NEWSLETTER SUBSCRIBERS ------------------------------
create table if not exists newsletter_subscribers (
  id bigint generated by default as identity primary key,
  email text unique not null,
  created_at timestamptz not null default now()
);

alter table newsletter_subscribers enable row level security;

create policy "Public can subscribe"
on newsletter_subscribers for insert
to anon
with check (true);
