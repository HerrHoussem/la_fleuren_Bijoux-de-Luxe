-- 1. PRODUCTS -----------------------------------------
create table if not exists products (
  id bigint generated by default as identity primary key,
  name text not null,
  category text,
  price numeric(10,2) not null,
  icon text,              -- emoji fallback shown when there's no photo, e.g. '📿'
  image_url text,         -- optional real product photo URL
  badge text,              -- small label e.g. 'Best seller', 'New', 'Gift'
  in_stock boolean not null default true,
  sort_order int not null default 0,
  created_at timestamptz not null default now()
);

alter table products enable row level security;

-- Anyone can view products (needed for the storefront to load them)
create policy "Public can view products"
on products for select
to anon
using (true);

